# classes and objects


# user-defined  type
# a class

class Point(object):
    '''
     Represents a point in 2-D space
    '''


blank  = Point()  # reference to s Point object: instantiation
print(blank)  # <__main__.Point object at 0x1038a4ba8>

# attributes
blank.x = 3.0
blank.y = 4.0

print(blank)  # # <__main__.Point object at 0x1038a4ba8>
print(blank.x)
print(blank.y)











